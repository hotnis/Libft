#include "libft.h"

void	wdmatch(char *s1, char *s2)
{
	char *s;

	s = s1;
	while (*s2)
	{
		if (*s2 == *s1)
			s1++;
		s2++;
	}
	if (!(*s1))
		write(1, s, ft_strlen(s));
}
